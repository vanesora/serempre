SELECT [ProductID]
      ,[ProductName]
      ,[QuantityPerUnit]
      ,[UnitPrice]
      ,[UnitsInStock]
      ,[UnitsOnOrder]
      ,[ReorderLevel]
      ,[Discontinued]
	,[Suppliers].[SupplierID]
	,[CompanyName]
      ,[ContactName]
      ,[ContactTitle]
      ,[Address]
      ,[City]
      ,[Region]
      ,[PostalCode]
      ,[Country]
      ,[Phone]
FROM Products
JOIN [Suppliers] ON Suppliers.SupplierID = Products.SupplierID
WHERE CategoryID = @categoryID